// Resolve a company name to a best-guess website domain for favicon lookup.
// Returns the curated value when present; otherwise a heuristic; null for the
// 'Other / Not listed' escape hatch (and empty/falsy input).
export function companyDomain(name) {
  if (!name) return null;
  const trimmed = String(name).trim();
  if (!trimmed || trimmed === 'Other / Not listed') return null;

  if (COMPANY_DOMAINS[trimmed]) return COMPANY_DOMAINS[trimmed];

  // Heuristic: lowercase, strip spaces and punctuation (& . , ' ( )), then
  // append '.com'. e.g. 'HDFC Bank' -> 'hdfcbank.com', 'AT&T' -> 'att.com'.
  const slug = trimmed
    .toLowerCase()
    .replace(/[\s&.,'()]/g, '');
  if (!slug) return null;
  return slug + '.com';
}
